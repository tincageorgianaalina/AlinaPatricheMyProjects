package alinatinca;

public class DataTypes {
    public static void main(String[] args) {
        /* The most useful 5 data types are:
        String = " "
         */
        String brand = "Dacia";
        String model = "1301";
        String a = "3";
        String b = "4";
        System.out.println(a+b); //34
        //write with upper letter
        System.out.println(brand.toUpperCase());
        //write with lower case
        System.out.println(brand.toLowerCase());
        //find the size
        System.out.println(model.length());

        //integer = whole number
        int c = 3;
        int d = 4;
        System.out.println(c+d); //7

        //double = decimal number
        double price = 2300.50;

        //boolean = true(1) or false(0)
        boolean certified = false;

        //char = a single character: #, &, $, d, Y, 2,..  ' '
        char grade = 'A';




    }
}